db.serialize(() => {
  db.run(`
    CREATE TABLE IF NOT EXISTS unit_statuses (
      bloco TEXT NOT NULL,
      unidade TEXT NOT NULL,
      status TEXT NOT NULL,
      updated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      PRIMARY KEY (bloco, unidade)
    )
  `);

  const stmt = db.prepare(`
    INSERT OR IGNORE INTO unit_statuses (bloco, unidade, status)
    VALUES (?, ?, 'available')
  `);

  let total = 0;

  for (const bloco of Object.keys(BUILDING_DATA)) {
    const floors = BUILDING_DATA[bloco];
    for (const floor of Object.keys(floors)) {
      for (const unidade of floors[floor]) {
        stmt.run(String(bloco), String(unidade));
        total++;
      }
    }
  }

  stmt.finalize();

  db.all(`SELECT COUNT(*) as total FROM unit_statuses`, [], (err, rows) => {
    if (err) {
      console.error("Erro ao contar unidades:", err);
    } else {
      console.log(`Seed concluído. Registros no banco: ${rows[0].total}`);
    }
    db.close();
  });
});